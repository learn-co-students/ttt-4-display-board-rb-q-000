# Define display_board that accepts a board and prints
# out the current state.
def display_board(arr)
  print " #{arr[0]} ", "|", " #{arr[1]} ", "|" " #{arr[2]} " "\n"
  print "-----------" "\n"
  print " #{arr[3]} ", "|", " #{arr[4]} ", "|" " #{arr[5]} " "\n"
  print "-----------" "\n"
  print " #{arr[6]} ", "|", " #{arr[7]} ", "|" " #{arr[8]} " "\n"
end
